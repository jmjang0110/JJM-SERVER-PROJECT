void PrintMap() {
	for (int i = 0; i < HEIGHT; ++i) {
		for (int k = 0; k < WIDTH; ++k) {
			if (GameMap[i][k] == 42) {
				std::cout << "¡Ú ";
				continue;
			}
			switch (GameMap[i][k]) {
			case ROAD:
				std::cout << ". ";
				break;
			case OBSTACLE:
				std::cout << "¡â ";
				break;
			case START:
				std::cout << "S ";
				break;
			case END:
				std::cout << "E ";
				break;
			default:
				break;
			}
		}
		std::cout << '\n';
	}
}
